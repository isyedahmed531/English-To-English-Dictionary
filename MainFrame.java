    private void searchButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchButtonMouseClicked
        // TODO add your handling code here:
        
        searchWord search = new searchWord(searchBox.getSelectedItem().toString().trim());
        //
        //if(searchBox.getSelectedItem().equals(null)) 
        if(search.getWordType().equals("null") | searchBox.getSelectedItem().equals(null)) {
            statusResult.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(153, 0, 0), 3, true));
            statusResult.setText("  Status: NO RECORDS FOUND! ");
        }
        else {
            wordTypeField.setText(search.getWordType());
            System.out.println("Search Status: "+search.getWordType()+" | "+search.getDef());
            defField.setText(search.getDef());
            
            Date date = new Date();
            SimpleDateFormat formatter = new SimpleDateFormat("dd-MM-yyyy hh:mm:ss");
            //
            new historyWord(formatter.format(date), searchBox.getSelectedItem().toString().trim());
            //
            statusResult.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 51, 153), 3, true));
            statusResult.setText("  Status:  History updated! ");
        }
    }//GEN-LAST:event_searchButtonMouseClicked

