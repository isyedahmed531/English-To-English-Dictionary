    private void addToFavButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addToFavButtonMouseClicked
        // TODO add your handling code here:
        String searchWord = searchBox.getSelectedItem().toString().trim();
        if(!duplicate.duplicateFavorite(searchWord)) {
            Date date = new Date();
            SimpleDateFormat formatter = new SimpleDateFormat("dd-MM-yyyy hh:mm:ss");
            new favoriteWord(formatter.format(date), searchBox.getSelectedItem().toString().trim());
            statusResult.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 51, 153), 3, true));
            statusResult.setText("  Status: Word added to Favorite.");
        }
        else {
            statusResult.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(153, 0, 0), 3, true));
            statusResult.setText("  Status: Word Already added in Favorite! ");
            
        }
    }//GEN-LAST:event_addToFavButtonMouseClicked

